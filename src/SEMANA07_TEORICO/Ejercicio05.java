    private void tbnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnmostrarActionPerformed
        // TODO add your handling code here:
        int contador=0;
        ArrayList<Integer> listanueva =new ArrayList<>();
        for(Integer i:listanum){
            if(i<=10){
                listanueva.add(0);
                contador++;
            }else if(i<=20){
                listanueva.add(1);
                contador++;
            }else{
                listanueva.add(i);
            }
            
        }
        txafinal.setText(cadnum(listanueva));
        txtscamb.setText(String.valueOf(contador));
    }//GEN-LAST:event_tbnmostrarActionPerformed

