    int aleatorio(int max,int min){
        return (int)((max-min+1)*Math.random()+min);
    }
    String cadnum(ArrayList<Integer> lis){
        String cad="";
        for(Integer i:lis){
            cad+=i+"\n";
        }
        return cad;
    }

